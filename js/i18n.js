    // Map browser-reported locale → our supported set. Order matters: we
    // try the exact tag first, then the language part, then English.
    function pickLocale() {
        const stored = localStorage.getItem("divo_lang");
        if (stored && T[stored]) return stored;
        const candidates = [];
        const browser = navigator.language || "en";
        candidates.push(browser);
        candidates.push(browser.toLowerCase());
        candidates.push(browser.split("-")[0]);
        // Special-case Chinese: "zh-CN", "zh-Hans-*" → "zh-Hans".
        if (browser.toLowerCase().startsWith("zh")) candidates.push("zh-Hans");
        // Brazilian-Portuguese variants → "pt-BR"; everything else → English fallback.
        if (browser.toLowerCase().startsWith("pt")) candidates.push("pt-BR");
        for (const c of candidates) if (T[c]) return c;
        return "en";
    }

    function applyLocale(loc) {
        const dict = T[loc] || T.en;
        const fallback = T.en;
        document.documentElement.lang = loc;
        document.documentElement.dir = (loc === "ar") ? "rtl" : "ltr";
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.getAttribute("data-i18n");
            const txt = dict[key] !== undefined ? dict[key] : fallback[key];
            if (txt !== undefined) el.textContent = txt;
        });
        document.querySelectorAll("[data-i18n-attr]").forEach(el => {
            // "title:nav.features|aria-label:nav.features"
            const spec = el.getAttribute("data-i18n-attr");
            spec.split("|").forEach(pair => {
                const [attr, key] = pair.split(":");
                const txt = dict[key] !== undefined ? dict[key] : fallback[key];
                if (txt !== undefined) el.setAttribute(attr, txt);
            });
        });
        localStorage.setItem("divo_lang", loc);
        // Update the picker label.
        const picker = document.getElementById("lang-picker");
        if (picker) picker.value = loc;
    }

    function buildPicker() {
        const picker = document.getElementById("lang-picker");
        if (!picker) return;
        picker.innerHTML = LOCALES.map(([code, name, flag]) =>
            `<option value="${code}">${flag}  ${name}</option>`
        ).join("");
        picker.addEventListener("change", e => applyLocale(e.target.value));
    }

    document.addEventListener("DOMContentLoaded", () => {
        buildPicker();
        applyLocale(pickLocale());
    });
